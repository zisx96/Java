
// 	TreeSet -> from set -> BST -> Balanced and Skewed, duplicates not allowed

import java.util.TreeSet;

public class TreeSet_05 {

	public static void main(String[] args) {
		
		TreeSet ts = new TreeSet();
		
		ts.add(10);
		ts.add(6);
		ts.add(30);
		ts.add(5);
		ts.add(50);
		ts.add(11);
		
		System.out.println(ts);	// ascending sorted 

	}

}
